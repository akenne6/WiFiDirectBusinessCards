    /**
     * A simple server socket that accepts connection and writes some data on
     * the stream.
     */
    public static class FileServerAsyncTask extends AsyncTask<Void, Void, String> {

        private Context context;
        private TextView statusText;

        /**
         *
         */
        public FileServerAsyncTask(Context context) {
            this.context = context;
        }

        @Override
        protected String doInBackground(Void... params) {
            try {
                while(true) {
                    ServerSocket serverSocket = new ServerSocket(SERVER_PORT);
                    Log.d("p2p", "Server: Socket opened");
                    Socket client = serverSocket.accept();
                    Log.d("p2p", "Server: connection done");
                    final FileOutputStream f = context.openFileOutput("vcards.xml", MODE_APPEND);

                    Log.d("p2p", "server: copying files " + f.toString());
                    InputStream inputstream = client.getInputStream();
                    copyFile(inputstream, f);
                    serverSocket.close();
                    return f.toString();
                }
            } catch (IOException e) {
                Log.e("p2p", e.getMessage());
                return null;
            }
        }

        /*
         * (non-Javadoc)
         * @see android.os.AsyncTask#onPostExecute(java.lang.Object)
         */
        @Override
        protected void onPostExecute(String result) {
            Log.e("p2p", "Done with the file transfer. Look at for a toast.");
            if (result != null) {
                Toast.makeText(context.getApplicationContext(),"Download has Finished", Toast.LENGTH_LONG).show();
            }

        }

        /*
         * (non-Javadoc)
         * @see android.os.AsyncTask#onPreExecute()
         */


    }
    public static boolean copyFile(InputStream inputStream, OutputStream out) {
        byte buf[] = new byte[1024];
        int len;
        try {
            while ((len = inputStream.read(buf)) != -1) {
                out.write(buf, 0, len);

            }
            out.close();
            inputStream.close();
        } catch (IOException e) {
            Log.d("p2p", e.toString());
            return false;
        }
        return true;
    }
